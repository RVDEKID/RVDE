    var msnry = new Masonry('.gallery', {
    itemSelector: '.gallery-item',
    columnWidth: '.gallery-item',
    percentPosition: true,
    gutter: 10,
  });
  
  // Stop the loop after 10 iterations
  count++;
  if (count >= 5) {
    clearInterval(interval);
  }
